    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed

        int id = Integer.parseInt(jTextField13.getText());
        if (score.isIdExist(id)) {
            double score1 = Double.parseDouble(jTextScore1.getText());
            double score2 = Double.parseDouble(jTextScore2.getText());
            double score3 = Double.parseDouble(jTextScore3.getText());
            double score4 = Double.parseDouble(jTextScore4.getText());
            double score5 = Double.parseDouble(jTextScore5.getText());
            double score6 = Double.parseDouble(jTextScore6.getText());
            double score7 = Double.parseDouble(jTextScore7.getText());
            double score8 = Double.parseDouble(jTextScore8.getText());
            double score9 = Double.parseDouble(jTextScore9.getText());
            double score10 = Double.parseDouble(jTextScore10.getText());

            // Calculate the grade based on the subject scores
            double[] subjectScores = {score1, score2, score3, score4, score5, score6, score7, score8, score9, score10};
            String grade = OverallGradeCalculator.calculateGrade(subjectScores);

            // Update the score with the calculated grade
            score.update(id, score1, score2, score3, score4, score5, score6, score7, score8, score9, score10, grade);

            jTable3.setModel(new DefaultTableModel(new Object[]{"ID", "Student ID", "Form", "Subject 1", "Score 1",
                "Subject 2", "Score 2", "Subject 3", "Score 3", "Subject 4", "Score 4", "Subject 5", "Score 5", "Subject 6", "Score 6", "Subject 7", "Score 7", "Subject 8", "Score 8", "Subject 9", "Score 9", "Subject 10", "Score 1,", "Grade"}, 0));
            score.getScoreValues(jTable3, "");
            clearScore();
        } else {
            JOptionPane.showMessageDialog(this, "Score id doesn't exist");
        }


    }//GEN-LAST:event_jButton23ActionPerformed

