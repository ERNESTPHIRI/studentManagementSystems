    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        if (jTextField11.getText().isEmpty() || jComboBox5.getItemCount() == 0) {
            JOptionPane.showMessageDialog(this, "Student id or Semester is missing");
        } else {
            int id = course.getMax(); // Ensure id initialization
            int sid = Integer.parseInt(jTextField11.getText());
            int semesterNo = Integer.parseInt(jComboBox5.getSelectedItem().toString());

            String[] subjects = {
                jComboBox6.getSelectedItem().toString(),
                jComboBox7.getSelectedItem().toString(),
                jComboBox8.getSelectedItem().toString(),
                jComboBox9.getSelectedItem().toString(),
                jComboBox10.getSelectedItem().toString(),
                jComboBox11.getSelectedItem().toString(),
                jComboBox12.getSelectedItem().toString(),
                jComboBox13.getSelectedItem().toString(),
                jComboBox14.getSelectedItem().toString(),
                jComboBox15.getSelectedItem().toString()
            };

            boolean courseTaken = false;
            if (semesterNo <= 4) {
                for (String subject : subjects) {
                    if (!subject.isEmpty() && course.isSubjectExist(sid, semesterNo, subject)) {
                        JOptionPane.showMessageDialog(this, "This student has already taken " + subject + " in Semester " + semesterNo);
                        courseTaken = true;
                        break;
                    }
                }
            } else {
                // Student has completed four semesters, prevent further course registrations
                JOptionPane.showMessageDialog(this, "This student has successfully completed all four semesters.");
                courseTaken = true;
            }

            if (!courseTaken) {
                course.insert(id, sid, semesterNo, subjects[0], subjects[1],
                        subjects[2], subjects[3], subjects[4], subjects[5], subjects[6],
                        subjects[7], subjects[8], subjects[9]);
                jTable2.setModel(new DefaultTableModel(new Object[]{"ID", "Student ID", "Form", "Subject 1",
                    "Subject 2", "Subject 3", "Subject 4", "Subject 5", "Subject 6", "Subject 7", "Subject 8", "Subject 9", "Subject 10"}, 0));
                course.getCourseValues(jTable2, "");

                clearCource();

            }
        }
    }//GEN-LAST:event_jButton13ActionPerformed

